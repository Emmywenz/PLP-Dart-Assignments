// Dart program demonstrating data types

void main() {
  // Integer data type
  int age = 30;
  print('Age: $age');

  // Double data type
  double weight = 65.5;
  print('Weight: $weight');

  // String data type
  String name = 'Alice';
  print('Name: $name');

  // List data type
  List<int> numbers = [1, 2, 3, 4, 5];
  print('Numbers: $numbers');

  // Map data type
  Map<String, String> person = {
    'name': 'Bob',
    'age': '25',
    'city': 'New York'
  };
  print('Person: $person');
}
